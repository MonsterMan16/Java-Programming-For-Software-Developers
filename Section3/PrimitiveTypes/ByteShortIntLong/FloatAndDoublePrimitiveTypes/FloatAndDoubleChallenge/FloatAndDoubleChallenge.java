package Section3.PrimitiveTypes.ByteShortIntLong.FloatAndDoublePrimitiveTypes.FloatAndDoubleChallenge;

public class FloatAndDoubleChallenge {
    public static void main(String[] args) {
        double numberOfPounds = 200;
        double poundsInKilograms = numberOfPounds * 0.45359237d;
        System.out.println(numberOfPounds + " in kilograms is: " + poundsInKilograms);

        double pi = 3.1415927d;
        double anotherNumber = 3_000_000.4_567_890d;
        System.out.println(pi);
        System.out.println(anotherNumber);
    }
}
